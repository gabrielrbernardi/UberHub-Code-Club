#include <bits/stdc++.h>
using namespace std;

int main(){
    int x = 4;
    float y = -12.63;
    char c = 'A';

    printf("x eh: %d\n", x);
    printf("y eh: %f\n", y);
    printf("c eh: %c\n", c);

    cout << "x eh: " << x << endl;
    cout << "y eh: " << y << endl;
    cout << "c eh: " << c << endl;
}
